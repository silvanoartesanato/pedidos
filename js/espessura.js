export default function espessura(espe){
    switch (espe){
        case 3:
            return 14.5 // preço 3mm
        case 6:
            return 25 // preço 6mm
        case 9:
            return 34 // preço 9mm
        case 12:
            return 50 // preço 12mm
        case 15:
            return 57 // preço 15mm
    }
}